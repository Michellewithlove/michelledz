using System;

class Программа
{
    static void ОсновнойМетод()
    {
        Console.WriteLine("Пожалуйста, введите целое число:");

        try // Этот блок используется для обработки возможных исключений
        {
            string ввод = Console.ReadLine();
            int число = ПреобразоватьВЦелое(ввод);
            Console.WriteLine($"Вы ввели число: {число}");
        }
        catch (FormatException) // Обрабатываем ситуацию, если ввод не является числом
        {
            Console.WriteLine("Ошибка: вы ввели не число.");
        }
        catch (OverflowException) // Обрабатываем ошибку переполнения
        {
            Console.WriteLine("Ошибка: введенное число выходит за пределы допустимого диапазона.");
        }
    }

    static int ПреобразоватьВЦелое(string ввод)
    {
        return int.Parse(ввод); // Преобразование строки в целочисленное значение
    }
}